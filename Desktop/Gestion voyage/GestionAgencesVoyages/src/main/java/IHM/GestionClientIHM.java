    private void cmdDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdDelActionPerformed
        // Récupérer la ligne sélectionnée
    int selectedRow = jTable1.getSelectedRow();
    
    if(selectedRow == -1) {
        JOptionPane.showMessageDialog(this, 
            "Veuillez sélectionner un client à supprimer", 
            "Aucune sélection", 
            JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // Confirmation de suppression
    int confirm = JOptionPane.showConfirmDialog(
        this, 
        "Êtes-vous sûr de vouloir supprimer ce client ?",
        "Confirmation de suppression",
        JOptionPane.YES_NO_OPTION);
    
    if(confirm != JOptionPane.YES_OPTION) return;
    
    // Récupérer le CIN du client
    String cin = (String) tableModel.getValueAt(selectedRow, 0);
    
    // Appeler le contrôleur
    boolean result = clientController.supprimerClient(cin);
    
    if(result) {
        JOptionPane.showMessageDialog(this, 
            "Client supprimé avec succès", 
            "Succès", 
            JOptionPane.INFORMATION_MESSAGE);
        
        // Rafraîchir la liste
        listerClients();
        clearFields();
    } else {
        JOptionPane.showMessageDialog(this, 
            "Échec de la suppression", 
            "Erreur", 
            JOptionPane.ERROR_MESSAGE); 
    }
    }//GEN-LAST:event_cmdDelActionPerformed

