    private void cmdAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdAddActionPerformed
           // Récupération des valeurs depuis les champs
    String cin = txtCin.getText().trim();
    String nom = txtNom.getText().trim();
    String prenom = txtPrenom.getText().trim();
    String passport = txtPassport.getText().trim();
    String telephone = txtTel.getText().trim();

    // Vérification des champs obligatoires
    if (cin.isEmpty() || nom.isEmpty() || prenom.isEmpty() || passport.isEmpty() || telephone.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Veuillez remplir tous les champs obligatoires.");
        return;
    }

    // Création du client
    Client client = new Client(
        cin,
        nom,
        prenom,
        passport,
        telephone
    );

    // Création du client via le contrôleur
        System.out.println(client);
    boolean isCreated = clientController.ajouterClient(client);

    if (isCreated) {
        JOptionPane.showMessageDialog(null, "Client ajouté avec succès.");
        
        // Vider les champs après création
        txtCin.setText("");
        txtNom.setText("");
        txtPrenom.setText("");
        txtPassport.setText("");
        txtTel.setText("");
        
        // Rafraîchir la liste
        listerClients();
    } else {
        JOptionPane.showMessageDialog(null, "Erreur lors de l'ajout du client.");
    }
    }//GEN-LAST:event_cmdAddActionPerformed

    private void cmdUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdUpdateActionPerformed
    // Récupérer la ligne sélectionnée
    int selectedRow = jTable1.getSelectedRow();
    
    if(selectedRow == -1) {
        JOptionPane.showMessageDialog(this, 
            "Veuillez sélectionner un client à modifier", 
            "Aucune sélection", 
            JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // Récupérer le CIN original (ne peut pas être modifié)
    String originalCin = (String) tableModel.getValueAt(selectedRow, 0);
    
    // Récupérer les nouvelles valeurs
    String nom = txtNom.getText().trim();
    String prenom = txtPrenom.getText().trim();
    String passport = txtPassport.getText().trim();
    String telephone = txtTel.getText().trim();

    // Validation basique
    if(nom.isEmpty() || prenom.isEmpty() || passport.isEmpty() || telephone.isEmpty()) {
        JOptionPane.showMessageDialog(this, 
            "Tous les champs doivent être remplis", 
            "Champs manquants", 
            JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Créer l'objet client modifié
    Client clientModifie = new Client(
        originalCin, // CIN reste inchangé
        nom,
        prenom,
        passport,
        telephone
    );

    // Appeler le contrôleur
    boolean result = clientController.modifierClient(clientModifie);
    
    if(result) {
        JOptionPane.showMessageDialog(this, 
            "Client modifié avec succès", 
            "Succès", 
            JOptionPane.INFORMATION_MESSAGE);
        
        // Rafraîchir la liste
        listerClients();
        clearFields();
    } else {
        JOptionPane.showMessageDialog(this, 
            "Échec de la modification", 
            "Erreur", 
            JOptionPane.ERROR_MESSAGE);
    }
                                     
    }//GEN-LAST:event_cmdUpdateActionPerformed

